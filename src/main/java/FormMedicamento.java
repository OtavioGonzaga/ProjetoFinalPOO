    @Override
    public final void limparFormulario() {
        nomeInput.setText("");
        nomeInput.repaint();
        nomeInput.revalidate();

        precoInput.setText("");
        precoInput.repaint();
        precoInput.revalidate();

        principioAtivoInput.setText("");
        principioAtivoInput.repaint();
        principioAtivoInput.revalidate();

        dosagemInput.setText("");
        dosagemInput.repaint();
        dosagemInput.revalidate();

        generoSelect.setSelectedIndex(0);
        generoSelect.repaint();
        generoSelect.revalidate();

        FormularioComponente[] subFormularios = new FormularioComponente[] {
                comumPanel, genericoPanel, controladoPanel, fitoterapicoPanel
        };

        for (FormularioComponente subFormulario : subFormularios) {
            subFormulario.limparFormulario();
        }
        this.repaint();
        this.revalidate();
    }

