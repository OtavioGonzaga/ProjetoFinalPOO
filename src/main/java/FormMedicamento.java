    @Override
    public void setarDados(Medicamento medicamento)
            throws NumeroNegativoException, NumberFormatException, IllegalArgumentException {
        String dosagemMg = this.dosagemInput.getText();
        String nome = this.nomeInput.getText();
        String preco = this.precoInput.getText();
        String principioAtivo = this.principioAtivoInput.getText();

        if (nome.isBlank()) {
            throw new IllegalArgumentException("Preencha o campo \"Nome\"");
        }

        if (preco.isBlank()) {
            throw new IllegalArgumentException("Preencha o campo \"Preço\"");
        }

        if (principioAtivo.isBlank()) {
            throw new IllegalArgumentException("Preencha o campo \"Princípio ativo\"");
        }

        if (dosagemMg.isBlank()) {
            throw new IllegalArgumentException("Preencha o campo \"Dosagem\"");
        }

        medicamento.setNome(nome);
        medicamento.setPrincipioAtivo(principioAtivo);

        try {
            medicamento.setDosagemMg(Float.parseFloat(dosagemMg));
        } catch (NumberFormatException nfe) {
            throw new NumberFormatException("O campo \"Dosagem\" precisa ser um número");
        }
        try {
            medicamento.setPreco(Float.parseFloat(preco));
        } catch (NumberFormatException nfe) {
            throw new NumberFormatException("O campo \"Preço\" precisa ser um número");
        }
    }

